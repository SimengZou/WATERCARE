create or replace TABLE DIM_ROUTE (
	ROUTE_SID NUMBER(38,0) NOT NULL autoincrement,
	ROUTE_KEY NUMBER(38,0),
	ROUTE_ID VARCHAR(16),
	ROUTE_DESCRIPTION VARCHAR(254),
	LAST_READ_DATE DATE,
	NEXT_READ_DATE DATE,
	BILLING_ROUTE_FLAG VARCHAR(1),
	OUT_FOR_ACTIVITY_FLAG VARCHAR(1),
	READING_CYCLE VARCHAR(10),
	TIME_INTERVAL NUMBER(38,0),
	TIME_INTERVAL_UNIT VARCHAR(1),
	ETL_SOURCE_DATETIME TIMESTAMP_NTZ(9),
	ETL_RECORD_CREATED TIMESTAMP_NTZ(9),
	ETL_RECORD_UPDATED TIMESTAMP_NTZ(9),
	constraint UNIQUE_ROUTE_KEY unique (ROUTE_KEY),
	primary key (ROUTE_SID)
);