, XSEWERMAIN.DESIGNRESILIENCE::VARCHAR(10) AS "Design Resilience"
, XSEWERMAIN.QUAKEDESIGN::VARCHAR(10) AS "Earthquake Design Level"
, XSEWERMAIN.EXTCOAT::VARCHAR(10) AS "External Coating"
, XSEWERMAIN.FUNCAREA::VARCHAR(10) AS "Functional Area"
, XSEWERMAIN.GRDLEVEL::FLOAT AS "Ground Level"
, XSEWERMAIN.INTLINING::VARCHAR(10) AS "Internal Lining"
, JTTYPE::VARCHAR(10) AS "Joint Method"
, XSEWERMAIN.LOCALITY::VARCHAR(10) AS "Locality"
, XSEWERMAIN.MEDIATYPE::VARCHAR(10) AS "Media Type"
, XSEWERMAIN.PHOTO3D::VARCHAR(300) AS "Photo/3D Model"
FROM 
 "DATAHUB_TARGET"."IPS_ASSETMANAGEMENT_SEWER_COMPSMN" AS A2
 INNER JOIN "DATAHUB_TARGET"."IPS_WSLASSETS_XALLASSETSDP02" AS A1 ON A2.COMPKEY = A1.COMPKEY AND A1.BUSINESSAREA = 'TCC'
 LEFT OUTER JOIN "DATAHUB_TARGET"."IPS_PROPERTY_ADDRESS" AS ad ON ad.ADDRKEY = a2.ADDRKEY AND ad.OPTA = 'TCC'
 LEFT OUTER JOIN DATAHUB_TARGET.IPS_WSLASSETSSEWER_XSEWERMAIN AS XSEWERMAIN ON A2.COMPKEY = XSEWERMAIN.COMPKEY 
 WHERE A2.BLDGFLOOR = 1002;