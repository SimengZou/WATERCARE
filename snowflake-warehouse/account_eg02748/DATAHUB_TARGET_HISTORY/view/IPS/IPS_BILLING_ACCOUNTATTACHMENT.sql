CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BILLING_ACCOUNTATTACHMENT AS 
    SELECT 
        ACCOUNTKEY, 
        ADDBY, 
        ADDDTTM, 
        ATTCHMTKEY, 
        DEFATTCHMT, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_ACCOUNTATTACHMENT
    UNION ALL 
    SELECT 
        ACCOUNTKEY, 
        ADDBY, 
        ADDDTTM, 
        ATTCHMTKEY, 
        DEFATTCHMT, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_ACCOUNTATTACHMENT;