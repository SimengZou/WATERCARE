CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_CRM_SRINSP AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        COMPDTTM, 
        DATALAKE_DELETED, 
        INSPECTR, 
        MODBY, 
        MODDTTM, 
        RESP, 
        SERVNO, 
        SRINSPKEY, 
        STARTDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CRM_SRINSP
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        COMPDTTM, 
        DATALAKE_DELETED, 
        INSPECTR, 
        MODBY, 
        MODDTTM, 
        RESP, 
        SERVNO, 
        SRINSPKEY, 
        STARTDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_CRM_SRINSP;