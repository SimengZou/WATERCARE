    SELECT 
        ACCESSID, 
        ADDBY, 
        ADDDTTM, 
        CODE, 
        DELETED, 
        DESCRIPT, 
        EFFDATE, 
        EXPDATE, 
        ISPORTALCLOSEDSTATUS, 
