CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BILLING_SUSPENDSERVICESETUP AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CREDITADJTYPESUKEY, 
        DEBITADJTYPESUKEY, 
        DELETED, 
        LOGTYPE, 
        MODBY, 
        MODDTTM, 
        SUSPENDSERVICESETUPKEYKEY, 
