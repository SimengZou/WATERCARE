CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_PORTAL_INSTRUMENT AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        DATALAKE_DELETED, 
        DATATYPECODE, 
        DATATYPECODEDESCRIPTION, 
        INSTRUMENTKEY, 
        LASTSAVE, 
        MEMBERSHIP, 
        MODBY, 
        MODDTTM, 
        PROJECTAPPLICATION, 
        SCENARIO, 
        TYPE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_PORTAL_INSTRUMENT
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        DATALAKE_DELETED, 
        DATATYPECODE, 
        DATATYPECODEDESCRIPTION, 
        INSTRUMENTKEY, 
        LASTSAVE, 
        MEMBERSHIP, 
        MODBY, 
        MODDTTM, 
        PROJECTAPPLICATION, 
        SCENARIO, 
        TYPE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_PORTAL_INSTRUMENT;