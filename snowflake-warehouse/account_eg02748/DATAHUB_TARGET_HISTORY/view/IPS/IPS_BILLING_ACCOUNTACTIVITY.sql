    SELECT 
        ACCOUNTKEY, 
        ACCOUNTSERVICEKEY, 
        ADDBY, 
        ADDDTTM, 
        ADJUSTMENTKEY, 
        APPFEEKEY, 
        BILLKEY, 
        DATALAKE_DELETED, 
        DEPOSITADJUSTMENTKEY, 
        DEPOSITCHARGEKEY, 
        LINEITEMKEY, 
        LINEITEMSETUPKEY, 
        MODBY, 
        MODDTTM, 
        ONEOFFCHARGEKEY, 
        PAYMENTKEY, 
        PENALTYKEY, 
        REFUNDKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_ACCOUNTACTIVITY
    UNION ALL 
    SELECT 
        ACCOUNTKEY, 
        ACCOUNTSERVICEKEY, 
        ADDBY, 
        ADDDTTM, 
        ADJUSTMENTKEY, 
        APPFEEKEY, 
        BILLKEY, 
        DATALAKE_DELETED, 
        DEPOSITADJUSTMENTKEY, 
        DEPOSITCHARGEKEY, 
        LINEITEMKEY, 
        LINEITEMSETUPKEY, 
        MODBY, 
        MODDTTM, 
        ONEOFFCHARGEKEY, 
        PAYMENTKEY, 
        PENALTYKEY, 
        REFUNDKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_ACCOUNTACTIVITY;