CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_RESOURCES_RESAVAIL AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        ALTWKDT, 
        DATALAKE_DELETED, 
        EFFDATE, 
        EMPID, 
        ENDTIME, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
        RESAVKEY, 
        STARTTIME, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        WKDAY, 
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_RESOURCES_RESAVAIL
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        ALTWKDT, 
        DATALAKE_DELETED, 
        EFFDATE, 
        EMPID, 
        ENDTIME, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
        RESAVKEY, 
        STARTTIME, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        WKDAY, 
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_RESOURCES_RESAVAIL;