    UNION ALL 
    SELECT 
        ACCOUNTNUMBER, 
        ADDBY, 
        ADDDTTM, 
        BILLKEY, 
        CREDITBALANCE, 
        DATALAKE_DELETED, 
        DEBITBALANCE, 
        MIGBALANCEKEY, 
        MODBY, 
        MODDTTM, 
