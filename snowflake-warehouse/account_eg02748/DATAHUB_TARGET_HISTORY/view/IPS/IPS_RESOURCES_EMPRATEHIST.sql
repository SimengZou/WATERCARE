CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_RESOURCES_EMPRATEHIST AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BENEFITRATE, 
        COMMENTS, 
        DATALAKE_DELETED, 
        EFFECTIVEDATE, 
        EMPLOYEE, 
        EMPRATEHISTKEY, 
        MODBY, 
        MODDTTM, 
        RATE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_RESOURCES_EMPRATEHIST
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BENEFITRATE, 
        COMMENTS, 
        DATALAKE_DELETED, 
        EFFECTIVEDATE, 
        EMPLOYEE, 
        EMPRATEHISTKEY, 
        MODBY, 
        MODDTTM, 
        RATE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_RESOURCES_EMPRATEHIST;