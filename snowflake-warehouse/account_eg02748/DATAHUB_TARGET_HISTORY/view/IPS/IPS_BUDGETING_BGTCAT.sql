CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BUDGETING_BGTCAT AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BGTDFNKEY, 
        CATDESC, 
        CATID, 
        CATKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PARENTKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BUDGETING_BGTCAT
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BGTDFNKEY, 
        CATDESC, 
        CATID, 
        CATKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PARENTKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BUDGETING_BGTCAT;