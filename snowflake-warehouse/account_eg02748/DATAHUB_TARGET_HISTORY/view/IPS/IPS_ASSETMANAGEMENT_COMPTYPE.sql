    SELECT 
        ADDBY, 
        ADDDTTM, 
        CLASS, 
        COMPCODE, 
        COMPDESC, 
        COMPTYPE, 
        DATALAKE_DELETED, 
        DETAILPAGECLASS, 
        EXPDATE, 
        MODBY, 
