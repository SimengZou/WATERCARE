        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_ASSETMANAGEMENT_COMPTYPE
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CLASS, 
        COMPCODE, 
        COMPDESC, 
        COMPTYPE, 
        DATALAKE_DELETED, 
        DETAILPAGECLASS, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
