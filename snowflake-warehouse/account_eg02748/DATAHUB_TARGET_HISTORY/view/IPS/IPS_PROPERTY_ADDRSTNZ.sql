        ADDBY, 
        ADDDTTM, 
        DELETED, 
        ESTNOHI, 
        ESTNOLO, 
        MODBY, 
        MODDTTM, 
        OSTNOHI, 
        OSTNOLO, 
        POSTCODE, 
