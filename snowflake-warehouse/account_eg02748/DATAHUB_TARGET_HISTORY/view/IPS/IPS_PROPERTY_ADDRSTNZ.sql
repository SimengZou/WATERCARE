        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_PROPERTY_ADDRSTNZ
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        ESTNOHI, 
        ESTNOLO, 
        MODBY, 
        MODDTTM, 
        OSTNOHI, 
        OSTNOLO, 
