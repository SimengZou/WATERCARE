    SELECT 
        ACCOUNTKEY, 
        ADDBY, 
        ADDDTTM, 
        APPROVALLEVEL, 
        COMMENTKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PRIORITY, 
        REFUNDAMOUNT, 
        REFUNDEDBY, 
        REFUNDEDDTTM, 
        REFUNDKEY, 
        REFUNDLINEITEMSETUPKEY, 
        REFUNDREASON, 
        REFUNDSTATUS, 
        REFUNDSTATUSBY, 
        REFUNDSTATUSDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_REFUND
    UNION ALL 
    SELECT 
        ACCOUNTKEY, 
        ADDBY, 
        ADDDTTM, 
        APPROVALLEVEL, 
        COMMENTKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PRIORITY, 
        REFUNDAMOUNT, 
        REFUNDEDBY, 
        REFUNDEDDTTM, 
        REFUNDKEY, 
        REFUNDLINEITEMSETUPKEY, 
        REFUNDREASON, 
        REFUNDSTATUS, 
        REFUNDSTATUSBY, 
        REFUNDSTATUSDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_REFUND;