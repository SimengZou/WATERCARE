CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BILLING_SERVICEFIELDVALUE AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        DESCRIPTION, 
        MODBY, 
        MODDTTM, 
        SERVICEFIELDKEY, 
        SERVICEFIELDVALUEKEY, 
        VALUE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_SERVICEFIELDVALUE
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        DESCRIPTION, 
        MODBY, 
        MODDTTM, 
        SERVICEFIELDKEY, 
        SERVICEFIELDVALUEKEY, 
        VALUE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_SERVICEFIELDVALUE;