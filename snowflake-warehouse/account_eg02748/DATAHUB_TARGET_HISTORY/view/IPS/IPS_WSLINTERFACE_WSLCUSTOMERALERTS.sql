CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_WSLINTERFACE_WSLCUSTOMERALERTS AS 
    SELECT 
        ACCOUNTALERTTYPE, 
        ACCOUNTNUMBER, 
        ADDBY, 
        ADDDTTM, 
        ADDRESSKEY, 
        DATALAKE_DELETED, 
        EFFECTIVEDATE, 
        EXPIREDATE, 
        MODBY, 
        MODDTTM, 
        VARIATION_ID, 
