        WSLCUSTOMERALERTSKEY, 
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_WSLINTERFACE_WSLCUSTOMERALERTS
    UNION ALL 
    SELECT 
        ACCOUNTALERTTYPE, 
        ACCOUNTNUMBER, 
        ADDBY, 
        ADDDTTM, 
        ADDRESSKEY, 
        DATALAKE_DELETED, 
        EFFECTIVEDATE, 
        EXPIREDATE, 
        MODBY, 
        MODDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        WSLCUSTOMERALERTSKEY, 
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_WSLINTERFACE_WSLCUSTOMERALERTS;