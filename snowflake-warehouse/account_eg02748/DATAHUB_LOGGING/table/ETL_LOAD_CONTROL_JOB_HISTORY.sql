create or replace TABLE ETL_LOAD_CONTROL_JOB_HISTORY (
	PROCESS_ID NUMBER(38,0) autoincrement,
	STG_PROCESS_NAME VARCHAR(50) NOT NULL,
	LOAD_PROCESS_NAME VARCHAR(50) NOT NULL,
	SCHEMA_NAME VARCHAR(50) NOT NULL,
	TABLE_NAME VARCHAR(50) NOT NULL,
	NEW_ETL_LOAD_DATETIME TIMESTAMP_NTZ(9),
	INC_ETL_LOAD_DATETIME TIMESTAMP_NTZ(9) NOT NULL,
	PREVIOUS_ETL_LOAD_DATETIME TIMESTAMP_NTZ(9),
	UPDATED_DATETIME TIMESTAMP_NTZ(9) NOT NULL,
	RECORD_ARCHIVED TIMESTAMP_NTZ(9) NOT NULL DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9)),
	primary key (STG_PROCESS_NAME, SCHEMA_NAME, TABLE_NAME, RECORD_ARCHIVED)
);