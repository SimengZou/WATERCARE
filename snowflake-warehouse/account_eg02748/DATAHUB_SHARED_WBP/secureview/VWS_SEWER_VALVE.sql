AS
SELECT 
  A1."ACQTYPE" AS "Acquistion Type"
, A2."ADDRKEY" AS "Address"
, A2."COMPKEY" AS "CompKey"
, A1."COSTCENTRE" AS "Cost Centre Code"
, 'TBD' AS "Cost Centre Description"
, A1."CRITICALITY" AS "Criticality Code"
, 'TBD' AS "Criticality Description"
, A2."UNITDESC" AS "Description"
, A1."FORDISPOSAL" AS "Disposal Reason"
, A1."DRAWINGNO" AS "Drawing No"
, A1."DRAWINGNOREF" AS "Drawing No Ref"
, A2."BGTNO" AS "Equipment No"
, A2."EXPDATE" AS "Expire Date"
, A2."AREA" AS "Facility Code"
, 'TBD' AS "Facility Description"
, A2."INSTDATE" AS "Installed"
, A2."ASBLT" AS "Legacy Key"
, A1."MODELNO" AS "Model #"
, A2."OWN" AS "Ownership Code"
, 'TBD' AS "Ownership Description"
, A2."MAPNO" AS "Position"
, A2."SUBAREA" AS "Process Code"
, 'TBD' AS "Process Description"
, A1."SAFETYCRITICAL" AS "Safety Critical Element"
, A2."SERVSTAT" AS "Service Status"
, A2."LOC" AS "Set Code"
, 'TBD' AS "Set Description"
, 'TBD' AS "Surface Cover Description"
, A2."DISTRICT" AS "System Code"
, 'TBD' AS "System Description"
, A2."UNITID" AS "Valve ID"
, A2."VALVESIZE" AS "Valve Size"
, A2."UNITTYPE" AS "Valve Type Code"
, 'TBD' AS "Valve Type Description"
, ad."NZHOUSENO" AS "Street Number"
, CONCAT(ad."NZSTNAME",' ',ad."NZSTTYPE") AS "Street Name"
, ad."NZSUBURB" AS "City"
, ad."NZPOSTCODE" AS "Postal Code"

, A1.MAKE                      						 AS "Make"
, A2.SERNO                                     		 AS "Serial Number"
, A1.ZONECATCHMENT             						 AS "Water Zone/Sewer Catchment"
, XSEWERVALVE.FOBFP        AS "Functional Output Backflow Preventer"
, XSEWERVALVE.FOBURSTCONTROL                         AS "Functional Output Burst Control"
, XSEWERVALVE.FOBYPASS                               AS "Functional Output Bypass"
, XSEWERVALVE.FODISCHARGE        AS "Functional Output Draindown Discharge"
, XSEWERVALVE.FOFIREFIGHTING                         AS "Functional Output Firefighting"
, XSEWERVALVE.FOFLOWCONTROL                          AS "Functional Output Flow Control"
, XSEWERVALVE.FOFLOWDIRECTION                        AS "Functional Output Flow Direction"
, XSEWERVALVE.FOISOLATION                            AS "Functional Output Isolation"
, XSEWERVALVE.FOLEVELCONTROL                         AS "Functional Output Level Control"
, XSEWERVALVE.FOPRESSRED                     AS "Functional Output Pressure Reducing"
, XSEWERVALVE.FOPRESSRLF                       AS "Functional Output Pressure Relief"
, XSEWERVALVE.FOPRESSSSTN                   AS "Functional Output Pressure Sustaining"
, XSEWERVALVE.FOREGULATING                           AS "Functional Output Regulating"
, XSEWERVALVE.FOSCOUR                                AS "Functional Output Scour"
, XSEWERVALVE.FOSURGE                                AS "Functional Output Surge Protection"
, XSEWERVALVE.FOVACB                          AS "Functional Output Vacuum Break"
, XSEWERVALVE.PRESSURERATING                         AS "Pressure Rating (kPa)"
, XSEWERVALVE.SUBTYPEFEATURES                        AS "SubType Features"
, A1.WARRANTYEND               						 AS "Warranty End Date"
, A1.WARRANTYSTART             						 AS "Warranty Start Date"
, DIR                                    AS "Direction To Open"
, VALVESTAT                                        AS "Valve Status"
, XSEWERVALVE.EXTCOAT                        AS "External Coating"
, XSEWERVALVE.FUNCAREA                         AS "Functional Area"
, XSEWERVALVE.HAZARDRATING                           AS "Hazardous Rating"
, XSEWERVALVE.IPRATING                               AS "Ingress Protection Rating"
, XSEWERVALVE.INSTALLMOUNT                      AS "Installation Mounting"
, XSEWERVALVE.INTLINING                         AS "Internal Lining"
, XSEWERVALVE.JTTYPE                              AS "Joint Method"
, XSEWERVALVE.LOCALITY                               AS "Locality"
, XSEWERVALVE.MAXFLOW                                AS "Maximum Flow"
, XSEWERVALVE.MINFLOW                                AS "Minimum Flow"
, XSEWERVALVE.MEDIATYPE                              AS "Media Type"
, XSEWERVALVE.PHOTO3D                                AS "Photo/3D Model"
, XSEWERVALVE.RESIDUALPRESSURE                       AS "Residual Pressure (kPa)"
, XSEWERVALVE.TORQUE                                 AS "Torque Input Rating"

FROM
  "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET"."IPS_ASSETMANAGEMENT_SEWER_COMPSV" as A2
  INNER JOIN "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET"."IPS_WSLASSETS_XALLASSETSDP02" as A1 ON A2.COMPKEY = A1.COMPKEY AND A1.BUSINESSAREA IN ('WBC','WBE','WBW')
  LEFT OUTER JOIN "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET"."IPS_PROPERTY_ADDRESS" as ad ON ad.ADDRKEY = a2.ADDRKEY AND ad.OPTA = 'WBP'
  LEFT OUTER JOIN "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET".IPS_WSLASSETSSEWER_XSEWERVALVE AS XSEWERVALVE ON A2.COMPKEY = XSEWERVALVE.COMPKEY 
  WHERE A2.BLDGFLOOR = 1003;