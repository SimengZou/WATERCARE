, XVALVE.HAZARDRATING                                      AS "Hazardous Rating"
, XVALVE.IPRATING                                          AS "Ingress Protection Rating"
, XVALVE.INSTALLMOUNT                                      AS "Installation Mounting"
, XVALVE.INTLINING                                         AS "Internal Lining"
, XVALVE.JTTYPE                                            AS "Joint Method"
, XVALVE.LOCALITY                                          AS "Locality"
, XVALVE.MAXFLOW                                           AS "Maximum Flow"
, XVALVE.MINFLOW                                           AS "Minimum Flow"
, XVALVE.MEDIATYPE                                         AS "Media Type"
, XVALVE.PHOTO3D                                           AS "Photo/3D Model"
, XVALVE.RESIDUALPRESSURE                                  AS "Residual Pressure (kPa)"
, XVALVE.TORQUE                                            AS "Torque Input Rating"

