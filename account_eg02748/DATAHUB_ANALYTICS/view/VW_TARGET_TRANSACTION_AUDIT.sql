create or replace view VW_TARGET_TRANSACTION_AUDIT(
	ACCOUNTKEY,
	SUM_OF_TXN_AMOUNT
) as
    SELECT 
        ACCTTRAN.accountkey,
        SUM(ACCTTRAN.tranamt) AS SUM_OF_TXN_AMOUNT
    FROM 
        DATAHUB_TARGET.IPS_BILLING_ACCTTRAN ACCTTRAN
    WHERE 
        DATE(ACCTTRAN.TRANDATE) BETWEEN '1900-01-01' and '2022-03-31'
    GROUP BY 
        ACCTTRAN.accountkey;