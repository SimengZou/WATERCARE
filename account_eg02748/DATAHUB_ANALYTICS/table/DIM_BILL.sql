create or replace TABLE DIM_BILL (
	BILL_SID NUMBER(38,0) NOT NULL autoincrement,
	BILL_KEY NUMBER(38,0),
	BILL_NUMBER VARCHAR(9),
	BILL_ESTIMATE_FLAG VARCHAR(1),
	BILL_PAID_STATUS VARCHAR(1),
	BILL_TYPE VARCHAR(8),
	BILL_DESCRIPTION VARCHAR(30),
	ADDRESS_LINE1 VARCHAR(40),
	ADDRESS_LINE2 VARCHAR(40),
	BILL_TO VARCHAR(90),
	BILL_TO_LINE2 VARCHAR(90),
	CITY VARCHAR(30),
	COUNTRY VARCHAR(20),
	STATE VARCHAR(30),
	ZIP VARCHAR(20),
	CREDIT_FLAG VARCHAR(1),
	DELINQUENT_DATE TIMESTAMP_NTZ(9),
	PREVIOUS_BILL_KEY NUMBER(38,0),
	ETL_SOURCE_DATETIME TIMESTAMP_NTZ(9),
	ETL_RECORD_CREATED TIMESTAMP_NTZ(9),
	ETL_RECORD_UPDATED TIMESTAMP_NTZ(9),
	primary key (BILL_SID)
);