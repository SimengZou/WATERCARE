create or replace TABLE DIM_READ_REASON (
	READ_REASON_SID NUMBER(38,0) NOT NULL autoincrement,
	READ_REASON_CODE VARCHAR(10),
	READ_REASON_DESCRIPTION VARCHAR(300),
	ETL_SOURCE_DATETIME TIMESTAMP_NTZ(9),
	ETL_RECORD_CREATED TIMESTAMP_NTZ(9),
	ETL_RECORD_UPDATED TIMESTAMP_NTZ(9),
	constraint UNIQUE_READ_REASON_CODE unique (READ_REASON_CODE),
	primary key (READ_REASON_SID)
);