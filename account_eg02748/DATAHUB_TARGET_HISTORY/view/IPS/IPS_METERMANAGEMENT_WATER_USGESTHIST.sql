CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_METERMANAGEMENT_WATER_USGESTHIST AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CYCLE, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        NUMEST, 
        ROUTE, 
        USGESTHISTDTTM, 
        USGESTHISTKEY, 
        USGESTRULEGRP, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_METERMANAGEMENT_WATER_USGESTHIST
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CYCLE, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        NUMEST, 
        ROUTE, 
        USGESTHISTDTTM, 
        USGESTHISTKEY, 
        USGESTRULEGRP, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_METERMANAGEMENT_WATER_USGESTHIST;