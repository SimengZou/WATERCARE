CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_CDR_BUILDING_BLDGBATCH AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APBLDGBATCHKEY, 
        APBLDGDEFNKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        SUBDIVCODE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CDR_BUILDING_BLDGBATCH
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APBLDGBATCHKEY, 
        APBLDGDEFNKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        SUBDIVCODE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_CDR_BUILDING_BLDGBATCH;