CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_ASSETMANAGEMENT_ASSETPOSITION AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        ADDRKEY, 
        ASSETPOSITIONKEY, 
        COMPTYPE, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        POSITIONS, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_ASSETMANAGEMENT_ASSETPOSITION
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        ADDRKEY, 
        ASSETPOSITIONKEY, 
        COMPTYPE, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        POSITIONS, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_ASSETMANAGEMENT_ASSETPOSITION;