    SELECT 
        ADDBY, 
        ADDDTTM, 
        BUDGETSEGMENTDEFKEY, 
        DATALAKE_DELETED, 
        DESCRIPTION, 
        LENGTH, 
        MODBY, 
        MODDTTM, 
        POSITION, 
        SEPARATOR, 
        TOTALLENGTH, 
