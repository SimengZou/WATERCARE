        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_RESOURCES_BUDGETSEGMENTDEF
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BUDGETSEGMENTDEFKEY, 
        DATALAKE_DELETED, 
        DESCRIPTION, 
        LENGTH, 
        MODBY, 
        MODDTTM, 
        POSITION, 
        SEPARATOR, 
        TOTALLENGTH, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_RESOURCES_BUDGETSEGMENTDEF;