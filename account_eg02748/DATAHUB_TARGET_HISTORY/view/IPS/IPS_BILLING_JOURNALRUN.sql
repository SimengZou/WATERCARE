    SELECT 
        ADDBY, 
        ADDDTTM, 
        AVGTRANPERMINUTE, 
        COMMITNUMBER, 
        DELETED, 
        ENDOFRUNDTTM, 
        INITIATEDBY, 
        INITIATEDDTTM, 
        JOURNALRUNKEY, 
