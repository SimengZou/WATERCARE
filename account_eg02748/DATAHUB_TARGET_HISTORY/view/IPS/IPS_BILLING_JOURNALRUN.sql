        MODDTTM, 
        NUMBEROFATPROCESSED, 
        NUMBEROFDTPROCESSED, 
        PROCESSINGFLAG, 
        PROCWITHEXCEPTFLAG, 
        SCHEDULEKEY, 
        STARTOFRUNDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_JOURNALRUN;