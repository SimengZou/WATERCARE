CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BILLING_ARRANGEMENTTRANSACTION AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        ARRANGEMENTTRANSACTIONKEY, 
        BILLKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PAYMENTARRANGEMENTKEY, 
        PAYMENTKEY, 
        TRANSACTIONAMT, 
        TRANSACTIONBY, 
        TRANSACTIONDTTM, 
        TRANSACTIONTYPE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_ARRANGEMENTTRANSACTION
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        ARRANGEMENTTRANSACTIONKEY, 
        BILLKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PAYMENTARRANGEMENTKEY, 
        PAYMENTKEY, 
        TRANSACTIONAMT, 
        TRANSACTIONBY, 
        TRANSACTIONDTTM, 
        TRANSACTIONTYPE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_ARRANGEMENTTRANSACTION;