CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BILLING_CANCELREBILL AS 
    SELECT 
        ACCOUNTAREA, 
        ACCOUNTCLASS, 
        ACCOUNTGROUP, 
        ACCOUNTSUBGROUP, 
        ADDBY, 
        ADDDTTM, 
        BILLINGCYCLE, 
        BILLINGPERIODSTARTDATE, 
        BILLINGPERIODSTOPDATE, 
        BILLTYPEKEY, 
        CANCELREBILLKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        NUMBERBILLS, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_CANCELREBILL
    UNION ALL 
    SELECT 
        ACCOUNTAREA, 
        ACCOUNTCLASS, 
        ACCOUNTGROUP, 
        ACCOUNTSUBGROUP, 
        ADDBY, 
        ADDDTTM, 
        BILLINGCYCLE, 
        BILLINGPERIODSTARTDATE, 
        BILLINGPERIODSTOPDATE, 
        BILLTYPEKEY, 
        CANCELREBILLKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        NUMBERBILLS, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_CANCELREBILL;