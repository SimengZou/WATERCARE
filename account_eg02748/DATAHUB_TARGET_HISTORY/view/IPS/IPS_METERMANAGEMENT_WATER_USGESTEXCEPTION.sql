    SELECT 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        EXCEPTIONDESCRIPTION, 
        MODBY, 
        MODDTTM, 
        READINGKEY, 
        USGESTEXCEPTIONKEY, 
