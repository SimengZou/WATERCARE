        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_METERMANAGEMENT_WATER_USGESTEXCEPTION
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        EXCEPTIONDESCRIPTION, 
        MODBY, 
        MODDTTM, 
        READINGKEY, 
        USGESTEXCEPTIONKEY, 
        USGESTHISTKEY, 
