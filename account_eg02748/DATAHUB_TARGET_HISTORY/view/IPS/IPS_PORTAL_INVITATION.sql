CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_PORTAL_INVITATION AS 
    SELECT 
        ACCEPTEDBY, 
        ACCEPTEDDATETIME, 
        ADDBY, 
        ADDDTTM, 
        APPLICANTTYPE, 
        CANCELLEDBY, 
        CANCELLEDDATETIME, 
        CAPACITY, 
        DATALAKE_DELETED, 
        EXPIREDDATE, 
        INSTRUMENTNUMBER, 
        INVITATIONCODE, 
        INVITATIONKEY, 
        MODBY, 
        MODDTTM, 
        PRODUCTFAMILY, 
        REFERENCEINVITE, 
        SENDTO, 
        SENTBY, 
        SENTDATETIME, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_PORTAL_INVITATION
    UNION ALL 
    SELECT 
        ACCEPTEDBY, 
        ACCEPTEDDATETIME, 
        ADDBY, 
        ADDDTTM, 
        APPLICANTTYPE, 
        CANCELLEDBY, 
        CANCELLEDDATETIME, 
        CAPACITY, 
        DATALAKE_DELETED, 
        EXPIREDDATE, 
        INSTRUMENTNUMBER, 
        INVITATIONCODE, 
        INVITATIONKEY, 
        MODBY, 
        MODDTTM, 
        PRODUCTFAMILY, 
        REFERENCEINVITE, 
        SENDTO, 
        SENTBY, 
        SENTDATETIME, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_PORTAL_INVITATION;