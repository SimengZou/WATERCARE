CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_PORTAL_PROFILEPERMISSION AS 
    SELECT 
        ACCESSID, 
        ADDBY, 
        ADDDTTM, 
        DATALAKE_DELETED, 
        MODBY, 
        MODDTTM, 
        PRODUCTFAMILY, 
        PROFILE, 
        PROFILEPERMISSIONKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_PORTAL_PROFILEPERMISSION
    UNION ALL 
    SELECT 
        ACCESSID, 
        ADDBY, 
        ADDDTTM, 
        DATALAKE_DELETED, 
        MODBY, 
        MODDTTM, 
        PRODUCTFAMILY, 
        PROFILE, 
        PROFILEPERMISSIONKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_PORTAL_PROFILEPERMISSION;