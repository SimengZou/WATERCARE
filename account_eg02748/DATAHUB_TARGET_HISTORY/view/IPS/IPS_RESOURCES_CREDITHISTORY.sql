CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_RESOURCES_CREDITHISTORY AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APPLIEDDATE, 
        CREDITHISTORYKEY, 
        CREDITHISTORYSOURCE, 
        CREDITRATINGPOINTSCODE, 
        DATALAKE_DELETED, 
        IDENTITYKEY, 
        MODBY, 
        MODDTTM, 
        POINTS, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_RESOURCES_CREDITHISTORY
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APPLIEDDATE, 
        CREDITHISTORYKEY, 
        CREDITHISTORYSOURCE, 
        CREDITRATINGPOINTSCODE, 
        DATALAKE_DELETED, 
        IDENTITYKEY, 
        MODBY, 
        MODDTTM, 
        POINTS, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_RESOURCES_CREDITHISTORY;