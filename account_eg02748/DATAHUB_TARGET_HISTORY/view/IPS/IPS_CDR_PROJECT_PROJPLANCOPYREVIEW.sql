CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_CDR_PROJECT_PROJPLANCOPYREVIEW AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APPROJREVIEWKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PROJPLANCOPYKEY, 
        PROJPLANCOPYREVIEWKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CDR_PROJECT_PROJPLANCOPYREVIEW
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APPROJREVIEWKEY, 
        DELETED, 
        MODBY, 
        MODDTTM, 
        PROJPLANCOPYKEY, 
        PROJPLANCOPYREVIEWKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_CDR_PROJECT_PROJPLANCOPYREVIEW;