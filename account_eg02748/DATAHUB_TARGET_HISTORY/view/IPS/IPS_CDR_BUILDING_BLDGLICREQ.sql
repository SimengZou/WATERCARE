CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_CDR_BUILDING_BLDGLICREQ AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APBLDGKEY, 
        APBLDGLICREQKEY, 
        DELETED, 
        GROUPNO, 
        LICTYPE, 
        MODBY, 
        MODDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CDR_BUILDING_BLDGLICREQ
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APBLDGKEY, 
        APBLDGLICREQKEY, 
        DELETED, 
        GROUPNO, 
        LICTYPE, 
        MODBY, 
        MODDTTM, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_CDR_BUILDING_BLDGLICREQ;