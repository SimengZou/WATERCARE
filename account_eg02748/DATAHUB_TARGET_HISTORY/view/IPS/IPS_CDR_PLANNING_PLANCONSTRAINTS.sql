    SELECT 
        ADDBY, 
        ADDDTTM, 
        APTYPEKEY, 
        CONSTRAINTCONTROLTYPE, 
        CONSTRAINTNAME, 
        DATALAKE_DELETED, 
        MILESTONE, 
        MODBY, 
        MODDTTM, 
