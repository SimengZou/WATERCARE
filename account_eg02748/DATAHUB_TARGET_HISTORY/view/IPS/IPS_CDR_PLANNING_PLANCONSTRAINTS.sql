        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CDR_PLANNING_PLANCONSTRAINTS
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APTYPEKEY, 
        CONSTRAINTCONTROLTYPE, 
        CONSTRAINTNAME, 
        DATALAKE_DELETED, 
        MILESTONE, 
        MODBY, 
        MODDTTM, 
