CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_CRM_REQTYPCAT AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CATKEY, 
        CATNAME, 
        DATALAKE_DELETED, 
        MODBY, 
        MODDTTM, 
        PARENT, 
        PORTALDESCRIPTION, 
        PORTALORDER, 
        PORTALTITLE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CRM_REQTYPCAT
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CATKEY, 
        CATNAME, 
        DATALAKE_DELETED, 
        MODBY, 
        MODDTTM, 
        PARENT, 
        PORTALDESCRIPTION, 
        PORTALORDER, 
        PORTALTITLE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_CRM_REQTYPCAT;