CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BUDGETING_BGTDEFN AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BGTDESC, 
        BGTDFNKEY, 
        BGTID, 
        DELETED, 
        ENDDT, 
        MODBY, 
        MODDTTM, 
        STARTDT, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BUDGETING_BGTDEFN
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        BGTDESC, 
        BGTDFNKEY, 
        BGTID, 
        DELETED, 
        ENDDT, 
        MODBY, 
        MODDTTM, 
        STARTDT, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BUDGETING_BGTDEFN;