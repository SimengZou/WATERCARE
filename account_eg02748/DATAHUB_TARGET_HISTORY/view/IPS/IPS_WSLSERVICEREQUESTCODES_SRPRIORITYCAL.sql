CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_WSLSERVICEREQUESTCODES_SRPRIORITYCAL AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CODE, 
        COMPLETED, 
        DELETED, 
        DESCRIPT, 
        EFFDATE, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
        ONSITE, 
        SERVICERESTORED, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_WSLSERVICEREQUESTCODES_SRPRIORITYCAL
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        CODE, 
        COMPLETED, 
        DELETED, 
        DESCRIPT, 
        EFFDATE, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
        ONSITE, 
        SERVICERESTORED, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_WSLSERVICEREQUESTCODES_SRPRIORITYCAL;