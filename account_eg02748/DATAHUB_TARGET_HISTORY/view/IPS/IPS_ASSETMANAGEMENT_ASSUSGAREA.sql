CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_ASSETMANAGEMENT_ASSUSGAREA AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        AREAHISTKEY, 
        COMPKEY, 
        DATALAKE_DELETED, 
        INSTDT, 
        MODBY, 
        MODDTTM, 
        REMDT, 
        USGAREAKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_ASSETMANAGEMENT_ASSUSGAREA
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        AREAHISTKEY, 
        COMPKEY, 
        DATALAKE_DELETED, 
        INSTDT, 
        MODBY, 
        MODDTTM, 
        REMDT, 
        USGAREAKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_ASSETMANAGEMENT_ASSUSGAREA;