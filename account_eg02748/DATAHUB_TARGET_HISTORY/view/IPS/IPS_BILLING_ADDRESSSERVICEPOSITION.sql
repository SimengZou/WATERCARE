    SELECT 
        ADDBY, 
        ADDDTTM, 
        ADDRESSSERVICEKEY, 
        ADDRKEY, 
        ADDRSRVPOSITIONKEY, 
        ASSETTYPE, 
        CONSUMPTIONPERCENTAGE, 
        DATALAKE_DELETED, 
