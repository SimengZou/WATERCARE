CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_WSLPORTAL_CUSTWEBUSR AS 
    SELECT 
        ACCTKEY, 
        ADDBY, 
        ADDDTTM, 
        CUSTWEBUSRKEY, 
        DATALAKE_DELETED, 
        MODBY, 
        MODDTTM, 
        MULTIACCOUNT, 
        SERVNO, 
        USRID, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_WSLPORTAL_CUSTWEBUSR
    UNION ALL 
    SELECT 
        ACCTKEY, 
        ADDBY, 
        ADDDTTM, 
        CUSTWEBUSRKEY, 
        DATALAKE_DELETED, 
        MODBY, 
        MODDTTM, 
        MULTIACCOUNT, 
        SERVNO, 
        USRID, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_WSLPORTAL_CUSTWEBUSR;