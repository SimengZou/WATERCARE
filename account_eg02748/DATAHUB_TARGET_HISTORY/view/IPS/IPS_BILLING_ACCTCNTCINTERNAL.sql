CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_BILLING_ACCTCNTCINTERNAL AS 
    SELECT 
        ACCOUNTKEY, 
        ACCTCNTCINTERNALKEY, 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        EFFDATE, 
        EMPID, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
        ROLE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_BILLING_ACCTCNTCINTERNAL
    UNION ALL 
    SELECT 
        ACCOUNTKEY, 
        ACCTCNTCINTERNALKEY, 
        ADDBY, 
        ADDDTTM, 
        DELETED, 
        EFFDATE, 
        EMPID, 
        EXPDATE, 
        MODBY, 
        MODDTTM, 
        ROLE, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_BILLING_ACCTCNTCINTERNAL;