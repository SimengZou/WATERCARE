CREATE OR REPLACE VIEW DATAHUB_TARGET_HISTORY.IPS_CDR_PROJECT_PROJAPPLDETAILLOG AS 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APPROJAPPLDTLKEY, 
        DELETED, 
        DTLMODBY, 
        DTLMODDTTM, 
        MODBY, 
        MODDTTM, 
        PROJAPPLDETAILLOGKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET_HISTORY.IPS_DELETED_CDR_PROJECT_PROJAPPLDETAILLOG
    UNION ALL 
    SELECT 
        ADDBY, 
        ADDDTTM, 
        APPROJAPPLDTLKEY, 
        DELETED, 
        DTLMODBY, 
        DTLMODDTTM, 
        MODBY, 
        MODDTTM, 
        PROJAPPLDETAILLOGKEY, 
        VARIATION_ID, 
        VARIATION_ID AS ETL_SEQUENCE_NUMBER,
        FALSE AS ETL_IS_DELETED,
        ETL_LOAD_DATETIME,
        ETL_LOAD_METADATAFILENAME
    FROM DATAHUB_TARGET.IPS_CDR_PROJECT_PROJAPPLDETAILLOG;