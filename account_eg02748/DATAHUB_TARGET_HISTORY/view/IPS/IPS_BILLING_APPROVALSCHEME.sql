        ADDBY, 
        ADDDTTM, 
        APPROVALGROUP, 
        APPROVALSCHEMEKEY, 
        APPROVALSTYLE, 
        DELETED, 
        DESCRIPTION, 
        EARLYFINALAPPROVALFORMULA, 
