 A1."ACQTYPE"::VARCHAR(10) AS "Acquistion Type"
, A2."ADDRKEY"::NUMBER(38) AS "Address"
, A2."BARLDIAM"::FLOAT AS "Barrel Diameter"
, A2."COMPKEY"::NUMBER(38) AS "CompKey"
