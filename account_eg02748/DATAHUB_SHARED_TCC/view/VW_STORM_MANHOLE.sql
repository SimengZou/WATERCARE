, XSTORMMANHOLES.INTLINING::VARCHAR(10) AS "Internal Lining"
, XSTORMMANHOLES.LOADRATING::FLOAT AS "Load Rating (kN)"
, XSTORMMANHOLES.LOCALITY::VARCHAR(10) AS "Locality"
, XSTORMMANHOLES.MEDIATYPE::VARCHAR(10) AS "Media Type"
, XSTORMMANHOLES.PHOTO3D::VARCHAR(300) AS "Photo/3D Model"
FROM
 "DATAHUB_TARGET"."IPS_ASSETMANAGEMENT_STORM_COMPSTMH" AS A2
 INNER JOIN "DATAHUB_TARGET"."IPS_WSLASSETS_XALLASSETSDP02" AS A1 ON A2.COMPKEY = A1.COMPKEY AND A1.BUSINESSAREA = 'TCC'
 LEFT OUTER JOIN "DATAHUB_TARGET"."IPS_PROPERTY_ADDRESS" AS ad ON ad.ADDRKEY = a2.ADDRKEY AND ad.OPTA = 'TCC'
 LEFT OUTER JOIN DATAHUB_TARGET.IPS_WSLASSETSSTORM_XSTORMMANHOLES AS XSTORMMANHOLES ON A2.COMPKEY = XSTORMMANHOLES.COMPKEY 
 WHERE A2.BLDGFLOOR = 1002;
