, A1.ZONECATCHMENT::VARCHAR(10) AS "Water Zone/Sewer Catchment"
, XSTORMMANHOLES.BOLTDOWN::VARCHAR(1) AS "Boltdown"
, CVRTYPE::VARCHAR(10) AS "Cover Type"
, XSTORMMANHOLES.DIAMEXT::FLOAT AS "Diameter External (mm)"
, XSTORMMANHOLES.DIAMINT::FLOAT AS "Diameter Internal (mm)"
, XSTORMMANHOLES.HINGED::VARCHAR(1) AS "Hinged"
, XSTORMMANHOLES.INVLEVEL::FLOAT AS "Invert Level (M)"
, DROPMH::VARCHAR(1) AS "Is Drop Manhole"
, XSTORMMANHOLES.LIDLEVEL::FLOAT AS "Lid Level (M)"
, XSTORMMANHOLES.LOCKABLE::VARCHAR(1) AS "Lockable"
, XSTORMMANHOLES.REMOVABLE::VARCHAR(1) AS "Removable"
, XSTORMMANHOLES.SUBTYPEFEATURES::VARCHAR(10) AS "SubType Features"
