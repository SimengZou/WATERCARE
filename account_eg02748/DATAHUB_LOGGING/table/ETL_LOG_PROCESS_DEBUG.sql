create or replace TABLE ETL_LOG_PROCESS_DEBUG (
	LOG_DEBUG_ID NUMBER(38,0) autoincrement,
	PROCESS_EXECUTION_ID NUMBER(38,0),
	PROCESS_NAME VARCHAR(50) NOT NULL,
	LOG_DATETIME TIMESTAMP_NTZ(9) NOT NULL DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9)),
	DEBUG_MESSAGE VARCHAR(256)
);