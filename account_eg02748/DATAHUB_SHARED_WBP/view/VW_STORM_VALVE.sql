CREATE OR REPLACE VIEW ${buildvar.env}_WBP_SHARE.DATAHUB_SHARED_WBP.VW_STORM_VALVE
AS
SELECT
  A1."ACQTYPE" AS "Acquistion Type"
, A2."ADDRKEY" AS "Address"
, A2."COMPKEY" AS "CompKey"
, A1."COSTCENTRE" AS "Cost Centre Code"
, 'TBD' AS "Cost Centre Description"
, A1."CRITICALITY" AS "Criticality Code"
, 'TBD' AS "Criticality Description"
, A2."UNITDESC" AS "Description"
, A1."FORDISPOSAL" AS "Disposal Reason"
, A1."DRAWINGNO" AS "Drawing No"
, A1."DRAWINGNOREF" AS "Drawing No Ref"
, A2."BGTNO" AS "Equipment No"
, A2."EXPDATE" AS "Expire Date"
, A2."AREA" AS "Facility Code"
, 'TBD' AS "Facility Description"
, A2."INSTDATE" AS "Installed"
, A2."ASBLT" AS "Legacy Key"
, A1."MODELNO" AS "Model #"
, A2."OWN" AS "Ownership Code"
, 'TBD' AS "Ownership Description"
, A2."MAPNO" AS "Position"
, A2."SUBAREA" AS "Process Code"
, 'TBD' AS "Process Description"
, A1."SAFETYCRITICAL" AS "Safety Critical Element"
, A2."SERVSTAT" AS "Service Status"
, A2."LOC" AS "Set Code"
, 'TBD' AS "Set Description"
, 'Column SURF Does not exist' AS "Surface Cover Code"-- , A2."SURF" AS "Surface Cover Code"
, 'TBD' AS "Surface Cover Description"
, A2."DISTRICT" AS "System Code"
, 'TBD' AS "System Description"
, A2."UNITID" AS "Valve ID"
, A2."VALVESIZE" AS "Valve Size"
, A2."UNITTYPE" AS "Valve Type Code"
, 'TBD' AS "Valve Type Description"

, ad."NZHOUSENO" AS "Street Number"
, CONCAT(ad."NZSTNAME",' ',ad."NZSTTYPE") AS "Street Name"
, ad."NZSUBURB" AS "City"
, ad."NZPOSTCODE" AS "Postal Code"

, A1.MAKE                      						 AS "Make"
, A2.SERNO                                       	 AS "Serial Number"
, A1.ZONECATCHMENT             						 AS "Water Zone/Sewer Catchment"
, XSTORMVALVE.FOBFP                    				 AS "Functional Output Backflow Preventer"
, XSTORMVALVE.FOBURSTCONTROL                         AS "Functional Output Burst Control"
, XSTORMVALVE.FOBYPASS                               AS "Functional Output Bypass"
, XSTORMVALVE.FODISCHARGE                   		 AS "Functional Output Draindown Discharge"
, XSTORMVALVE.FOFIREFIGHTING                         AS "Functional Output Firefighting"
, XSTORMVALVE.FOFLOWCONTROL                          AS "Functional Output Flow Control"
, XSTORMVALVE.FOFLOWDIRECTION                        AS "Functional Output Flow Direction"
, XSTORMVALVE.FOISOLATION                            AS "Functional Output Isolation"
, XSTORMVALVE.FOLEVELCONTROL                         AS "Functional Output Level Control"
, XSTORMVALVE.FOPRESSRED                     		 AS "Functional Output Pressure Reducing"
, XSTORMVALVE.FOPRESSRLF                       		 AS "Functional Output Pressure Relief"
, XSTORMVALVE.FOPRESSSSTN                   	 	 AS "Functional Output Pressure Sustaining"
, XSTORMVALVE.FOREGULATING                           AS "Functional Output Regulating"
, XSTORMVALVE.FOSCOUR                                AS "Functional Output Scour"
, XSTORMVALVE.FOSURGE                                AS "Functional Output Surge Protection"
, XSTORMVALVE.FOVACB                          		 AS "Functional Output Vacuum Break"
, XSTORMVALVE.PRESSURERATING                         AS "Pressure Rating (kPa)"
, XSTORMVALVE.SUBTYPEFEATURES                        AS "SubType Features"
, A1.WARRANTYEND               						 AS "Warranty End Date"
, A1.WARRANTYSTART             						 AS "Warranty Start Date"
, DIR                                    			 AS "Direction To Open"
, VALVESTAT                                        	 AS "Valve Status"
, XSTORMVALVE.EXTCOAT                        		 AS "External Coating"
, XSTORMVALVE.FUNCAREA                         		 AS "Functional Area"
, XSTORMVALVE.HAZARDRATING                           AS "Hazardous Rating"
, XSTORMVALVE.IPRATING                               AS "Ingress Protection Rating"
, XSTORMVALVE.INSTALLMOUNT                      	 AS "Installation Mounting"
, XSTORMVALVE.INTLINING                         	 AS "Internal Lining"
, XSTORMVALVE.JTTYPE                              	 AS "Joint Method"
, XSTORMVALVE.LOCALITY                               AS "Locality"
, XSTORMVALVE.MAXFLOW                                AS "Maximum Flow"
, XSTORMVALVE.MINFLOW                                AS "Minimum Flow"
, XSTORMVALVE.MEDIATYPE                              AS "Media Type"
, XSTORMVALVE.PHOTO3D                                AS "Photo/3D Model"
, XSTORMVALVE.RESIDUALPRESSURE                       AS "Residual Pressure (kPa)"
, XSTORMVALVE.TORQUE                                 AS "Torque Input Rating"

FROM
  "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET"."IPS_ASSETMANAGEMENT_STORM_COMPSTV" as A2
  INNER JOIN "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET"."IPS_WSLASSETS_XALLASSETSDP02" as A1 ON A2.COMPKEY = A1.COMPKEY AND A1.BUSINESSAREA IN ('WBC','WBE','WBW')
  LEFT OUTER JOIN "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET"."IPS_PROPERTY_ADDRESS" as ad ON ad.ADDRKEY = a2.ADDRKEY AND ad.OPTA = 'WBP'
  LEFT OUTER JOIN "${buildvar.env}_WCC_DATAWAREHOUSE"."DATAHUB_TARGET".IPS_WSLASSETSSTORM_XSTORMVALVE AS XSTORMVALVE ON A2.COMPKEY = XSTORMVALVE.COMPKEY 
  WHERE A2.BLDGFLOOR = 1003;