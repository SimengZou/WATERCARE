CREATE OR REPLACE VIEW DATAHUB_LANDING.VW_EAM_R5USERFUNCTIONDEFS AS SELECT
                        src:UFN_DESCRIPTION::varchar AS UFN_DESCRIPTION, 
                        src:UFN_FUNCTIONNAME::varchar AS UFN_FUNCTIONNAME, 
                        src:UFN_LASTSAVED::datetime AS UFN_LASTSAVED, 
                        src:UFN_UPDATECOUNT::numeric(38, 10) AS UFN_UPDATECOUNT, 
                        src:_DELETED::boolean AS _DELETED, 
            CASE
                WHEN 'EAM' = 'LN'
                THEN src:"deleted"::BOOLEAN
                WHEN 'EAM' = 'IPS'
                THEN src:"DATALAKE_DELETED"::BOOLEAN
                ELSE src:"_DELETED"::BOOLEAN
            END as ETL_DELETED,
            etl_load_datetime,
            etl_load_metadatafilename,
            ETL_RANK,
            IFNULL(TRY_TO_TIMESTAMP(replace(right(replace(lower(etl_load_metadatafilename),'.json'),23),'_','-'), 'yyyy-mm-dd-HH-MI-SS-FF') ,etl_load_datetime) as ETL_RANK_TIMESTAMP
            FROM 
            (
            select 
                src,
                etl_load_datetime,
                etl_load_metadatafilename,
                ROWNUMBER as ETL_RANK
                from
                (
                    SELECT
    
                        
                src:UFN_FUNCTIONNAME,
            src:UFN_LASTSAVED
                ,src,
                etl_load_datetime,
                etl_load_metadatafilename,
                ROW_NUMBER() OVER (PARTITION BY 
                                        
                src:UFN_FUNCTIONNAME  ORDER BY 
            src:UFN_LASTSAVED desc,IFNULL(TRY_TO_TIMESTAMP(replace(right(replace(lower(etl_load_metadatafilename),'.json'),23),'_','-'), 'yyyy-mm-dd-HH-MI-SS-FF') ,etl_load_datetime) desc) as ROWNUMBER
                FROM DATAHUB_LANDING.EAM_R5USERFUNCTIONDEFS as strm))